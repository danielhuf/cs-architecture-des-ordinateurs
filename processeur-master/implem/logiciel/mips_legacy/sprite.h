typedef unsigned int uint32;

/* object type definition used for aliens, spaceship and laser */
typedef struct Object {
	uint32 alive;
	uint32 period;
	uint32 deadline;
	int x, y;
	int dx, dy;
	uint32 *pattern;
	uint32 color;
	uint32 bg[8][8]; /* background */
	int ax, ay;
} Object;

